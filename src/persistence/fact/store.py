"""Storage backends for the bitemporal datom log.

The :class:`Store` Protocol is the seam between the pure-data ``DB`` /
``DBView`` layer and whatever backend happens to be under the hood —
in-memory for tests, SQLite for zero-ops single-operator deploys, Postgres
for production.

Per agent1-fact-spec §4 the production layout uses 5 primary indexes
(EAVT/AEVT/AVET/VAET, plus VT-E for bitemporal ranges) and a log-ordered
index for ``since(t)`` replication. Those indexes are created by the SQL
migration in ``migrations/0001_datom_log.sql``; this module just loads that
file into any DB-API-2 connection.

Concurrency contract (ARIS Round 3 P-concurrency)
-------------------------------------------------
**GIL doesn't protect you — assume you're in a multi-worker deployment.**
The old ``next_tx()`` + ``append()`` pair is a TOCTOU race: two writers
can both read ``MAX(tx)=N`` before either commits and then both insert
with ``tx=N+1``. The GIL happens to serialise pure-Python paths under
light load, but any SQLite I/O releases it — and a multi-process
(gunicorn, uvicorn workers) deployment has no GIL at all between writers.

The authoritative atomic primitive is :meth:`Store.allocate_and_append`,
which allocates the next tx id AND appends all provided datoms in a
single critical section. On SQLite that section is a ``BEGIN IMMEDIATE``
transaction — the writer lock is grabbed before the ``MAX(tx)`` read so
the read and the insert are serialised against any other writer. On
InMemoryStore the section is held under a ``threading.Lock``. On Postgres
the same pattern maps onto a ``SELECT ... FOR UPDATE`` or a ``SERIAL`` /
``GENERATED BY DEFAULT AS IDENTITY`` column (Phase 2).

:meth:`Store.next_tx` remains on the Protocol for read-only inspection
(tests, admin probes) and is implemented as a thin wrapper that does NOT
allocate — it returns the id that the *next* ``allocate_and_append`` call
would assign. Callers that need a tx id MUST use ``allocate_and_append``
to get it atomically with the write; treating ``next_tx`` as an allocator
will race.
"""

