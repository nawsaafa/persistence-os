# SQLiteStore — persistent backend used by the test suite. The schema is the
# same one the Postgres migration produces, so switching backends is just a
# DSN change for any operator willing to run Postgres.
# ---------------------------------------------------------------------------
class SQLiteStore:
    """SQLite-backed datom log. Uses the shared migration SQL."""

    def __init__(self, path: str = ":memory:") -> None:
        self._path = path
        # check_same_thread=False is safe because we serialize via _lock;
        # SQLite itself handles concurrent readers fine.
        self._conn = sqlite3.connect(
            path, detect_types=sqlite3.PARSE_DECLTYPES, check_same_thread=False
        )
        self._conn.row_factory = sqlite3.Row
        # isolation_level=None → we manage transactions explicitly so
        # BEGIN IMMEDIATE actually starts a reserved-write transaction
        # (the default implicit mode would start a deferred one).
        self._conn.isolation_level = None
        # RLock matches InMemoryStore: _run() in persistence.txn holds this
        # across the conflict-check + db.transact() sequence; the inner
        # allocate_and_append re-acquires on the same thread.
        self._lock = threading.RLock()
        self._apply_migrations()

    def _apply_migrations(self) -> None:
        # executescript runs in its own auto-commit wrapper even with
        # isolation_level=None, so no explicit BEGIN needed here.
        for _name, sql in load_migrations():
            self._conn.executescript(sql)

    # ---- Protocol methods ------------------------------------------------
    def append(self, datoms: Iterable[Datom]) -> None:
        rows = [_encode(d) for d in datoms]
        if not rows:
            return
        with self._lock:
            try:
                self._conn.execute("BEGIN IMMEDIATE")
                self._conn.executemany(
                    """
                    INSERT INTO datom_log
                      (e, a, v, tx, tx_time, valid_from, valid_to, op,
                       provenance, invalidated_by)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    rows,
                )
                self._conn.execute("COMMIT")
            except BaseException:
                self._conn.execute("ROLLBACK")
                raise

    def allocate_and_append(self, datoms: Iterable[Datom]) -> list[Datom]:
        """Atomic allocate-and-append under ``BEGIN IMMEDIATE``.

        The SQLite writer lock is grabbed on ``BEGIN IMMEDIATE``, so the
        ``MAX(tx)`` read and the ``INSERT`` are serialised against any
        other concurrent writer even without the in-process
        :class:`threading.Lock` (which we keep anyway for tidy
        single-process behaviour).
        """
        materialised = list(datoms)
        if not materialised:
            return []
        with self._lock:
            try:
                self._conn.execute("BEGIN IMMEDIATE")
                cur = self._conn.execute(
                    "SELECT COALESCE(MAX(tx), 0) + 1 FROM datom_log"
                )
                row = cur.fetchone()
                tx = int(row[0] if row is not None else 1)
                out = [_with_tx(d, tx) for d in materialised]
                self._conn.executemany(
                    """
                    INSERT INTO datom_log
                      (e, a, v, tx, tx_time, valid_from, valid_to, op,
                       provenance, invalidated_by)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    [_encode(d) for d in out],
                )
                self._conn.execute("COMMIT")
            except BaseException:
                self._conn.execute("ROLLBACK")
                raise
        return out

    def all_datoms(self) -> Iterator[Datom]:
        with self._lock:
            cur = self._conn.execute(
                "SELECT * FROM datom_log ORDER BY seq ASC"
            )
            rows = cur.fetchall()
        return (_decode(r) for r in rows)

    def since(self, tx_time: datetime) -> Iterator[Datom]:
        t_iso = tx_time.isoformat()
        with self._lock:
            cur = self._conn.execute(
                "SELECT * FROM datom_log WHERE tx_time > ? ORDER BY seq ASC",
                (t_iso,),
            )
            rows = cur.fetchall()
        return (_decode(r) for r in rows)

    def mark_invalidated(self, tx: int, invalidated_by_tx: int) -> None:
        with self._lock:
            try:
                self._conn.execute("BEGIN IMMEDIATE")
                self._conn.execute(
                    """
                    UPDATE datom_log
                       SET invalidated_by = ?
                     WHERE tx = ?
                       AND invalidated_by IS NULL
                       AND op = 'assert'
                    """,
                    (invalidated_by_tx, tx),
                )
                self._conn.execute("COMMIT")
            except BaseException:
                self._conn.execute("ROLLBACK")
                raise

    def next_tx(self) -> int:
        """``SELECT COALESCE(MAX(tx), 0) + 1 FROM datom_log`` under lock.

        Read-only probe — see module docstring. To atomically reserve an
        id, use :meth:`allocate_and_append`.
        """
        with self._lock:
            cur = self._conn.execute(
                "SELECT COALESCE(MAX(tx), 0) + 1 FROM datom_log"
            )
            row = cur.fetchone()
        # Result is either a Row (when row_factory=Row) or a plain tuple.
        return int(row[0] if row is not None else 1)

    # ------- Additive Protocol methods (PG3 / ADR-15 W2) ------------------

    @contextmanager
    def _txn(self):
        """Default transaction context — SQLiteStore relies on per-method BEGIN IMMEDIATE.

        Each existing method (``append`` / ``allocate_and_append`` /
        ``mark_invalidated``) opens its own ``BEGIN IMMEDIATE`` writer-
        lock transaction and commits before returning. ``_txn()`` is a
        pass-through that yields ``None`` so the higher-level
        :meth:`transact_serializable` shape works without nesting two
        transactions (``BEGIN IMMEDIATE`` inside another ``BEGIN`` would
        raise on SQLite). Multi-step atomicity in single-process SQLite
        deployments is covered by the existing ``_lock`` mutex; the
        ``BEGIN IMMEDIATE`` boundary lives one level down on the actual
        write call.
        """
        with nullcontext():
            yield None

    def transact_serializable(
        self,
        datoms: Iterable[Datom],
        *,
        tx_time: datetime | None = None,
    ) -> int:
        """Default impl: route through :meth:`allocate_and_append` under ``_lock``.

        Per ADR-15 W2 the SQLiteStore default impl preserves existing
        single-process semantics — the in-process ``_lock`` mutex plus
        ``BEGIN IMMEDIATE`` writer-lock is the existing serialisation
        gate. When ``tx_time`` is provided, every datom's ``tx_time`` is
        overridden with it before insertion (ADR-13 preservation of the
        audit handler's ``recorded_at`` instant).
        """
        materialised = list(datoms)
        if not materialised:
            return 0
        if tx_time is not None:
            materialised = [_replace_tx_time(d, tx_time) for d in materialised]
        out = self.allocate_and_append(materialised)
        return int(out[0].tx)

    # ---- Connection management ------------------------------------------
    def close(self) -> None:
        with self._lock:
            self._conn.close()

    def __del__(self) -> None:  # best-effort cleanup
        try:
            self.close()
        except Exception:
            pass


